const QUALITY_GUARDS = `CRITICAL QUALITY REQUIREMENTS — violations will cause the document to be rejected:

1. HAZARD SPECIFICITY: Do NOT use generic hazards like "slip/trip/fall", "injury", "safety risks", or "general hazards". Every hazard must be specific to the work activity in that step. Ask yourself: "Could this hazard description apply to ANY job?" — if yes, make it more specific.

2. RISK REDUCTION: The residual risk rating MUST be strictly LOWER than the initial risk rating for every step. If your controls don't reduce the risk, add stronger controls or reconsider the initial rating. A step with "High" initial risk and "High" residual risk means your controls are useless.

3. HIERARCHY OF CONTROLS: Control measures MUST be listed in hierarchy order for each step. Start with elimination/substitution where possible, then engineering, then admin, then PPE. You MUST prefix each control with its type in square brackets: [ELIMINATE], [SUBSTITUTE], [ISOLATE], [ENGINEERING], [ADMIN], [PPE]. Do not list only PPE controls — there must always be higher-order controls attempted first.

4. STEP COUNT: Generate between 5 and 15 procedure steps. Each step must describe a single, discrete physical activity. "Preparation" is not a step — "Set up exclusion zone and erect safety barriers" is. "Completion" is not a step — "Remove all tools and materials, dismantle temporary barriers, conduct final site inspection" is.

5. HRCW ACCURACY: Only select HRCW categories that genuinely apply. A painting job on ground level has ZERO HRCW categories for height. An electrical job always includes category 7. Do not pad the list.

6. TOOLBOX TALK: The toolbox talk must be written in spoken language — contractions, direct address ("you", "we"), practical instructions. It is NOT a copy of the formal SWMS content. It's what a foreman would actually say to the crew.

7. EMERGENCY PROCEDURES: Must be specific to the identified hazards. A SWMS for electrical work must include electrical shock emergency response. A SWMS for work at height must include fall response. Do not include generic fire evacuation unless fire is an identified hazard.

8. PPE RELEVANCE: Only list PPE that is actually needed. A desk-based job does not need steel-capped boots. A painting job on ground level does not need a fall harness. Over-specifying PPE undermines credibility.`;

// ============================================================================
// Prompt Assembly
// ============================================================================

export interface SwmsPromptInput {
  state: AustralianState;
  job_description: string;
  site_address?: string;
  principal_contractor?: string;
  additional_hazards?: string[];
}

export function buildSwmsPrompt(input: SwmsPromptInput): {
  system: string;
  user: string;
} {
  const system = [
    SYSTEM_PROMPT_BASE,
    getStateLegislationPrompt(input.state),
    HRCW_REFERENCE,
    OUTPUT_SCHEMA_PROMPT,
    QUALITY_GUARDS,
  ].join("\n\n---\n\n");

  const additionalContext = input.additional_hazards?.length
    ? `\nAdditional hazards identified from site photo analysis (incorporate these into the relevant steps):\n${input.additional_hazards.map((h) => `- ${h}`).join("\n")}`
    : "";

  const user = `Generate a complete SWMS for the following job:

State/Territory: ${input.state}
Job Description: ${input.job_description}
Site Address: ${input.site_address || "Not specified"}
Principal Contractor: ${input.principal_contractor || "Not specified"}${additionalContext}

Respond with valid JSON only.`;

  return { system, user };
}

// ============================================================================
// Correction Prompt (used on retry after validation failure)
// ============================================================================

export function buildCorrectionPrompt(
  originalResponse: string,
  issues: string[]
): string {
  return `Your previous response had the following quality issues:

${issues.map((issue, i) => `${i + 1}. ${issue}`).join("\n")}

Please fix these issues and return the corrected JSON. Remember:
- Residual risk MUST be lower than initial risk for every step
- Controls must be prefixed with their type: [ELIMINATE], [SUBSTITUTE], [ISOLATE], [ENGINEERING], [ADMIN], [PPE]
- Hazards must be specific to each step, not generic
- HRCW categories must be from the valid list of 19

Original response (fix the issues above):
${originalResponse}

Return ONLY the corrected JSON.`;
}
